//Declaração de variáveis 

var n = 14;
var p = 21;

//Incrementos e decrementos

console.log(n);
console.log(--n);
console.log(--n);

console.log(p);
console.log(p++);
console.log(p++);
/*
++n(ou --n) -> começa a contagem já depois do valor da variável 
p++(ou p--) -> começa a contagem (a partir) do valor da variável
*/