/*Declaração de variáveis:
    variável do tipo 'var', de escopo global e aceita reatribuição de novo valor
    variável do tipo 'let', de escopo mais limitado e podendo ter reatribuição de valor se não repetir o 'let'
    variável do tipo 'const', que não aceita reatribuição de valor 
*/
var dificuldade = 'Normal';
let custo = 50;
const restricao = 'Cinco níveis';

/*
    Método de saída que determina o tipo de valor contido em cada uma das variáveis
*/
console.log(typeof dificuldade);
console.log(typeof custo);
console.log(typeof restricao);