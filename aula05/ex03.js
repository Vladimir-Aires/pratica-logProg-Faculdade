//Declaração de variáveis

var x = 2;
var y = '2';
var z = 'dois';
var nome = 'Vladimir ';
var sobrenome = 'Aires';
var nomecompleto = 'Vladimir Aires';
var idade = 20;

//Saída com operações lógicas entre cada valor das variáveis

console.log(x == y);
console.log(y === x);
console.log(z != x);
console.log(nome + sobrenome === nomecompleto);
console.log(z !== nomecompleto);
console.log(x == idade);

/*
    = -> equivalência substitutiva
    == -> equivalência aritmética
    === -> equivalência proposiocional estrita

    ! = (ou !=) -> diferença
    ! == (ou !==) -> estritamente diferente

*/